// ─────────────────────────────────────────────────────────────────────────────

/** If specified, payment collection for this subscription will be paused. */
export interface SubscriptionPauseCollection {
  /** The payment collection behavior for this subscription while paused. */
  behavior: 'keep_as_draft' | 'mark_uncollectible' | 'void';
  /** The time after which the subscription will resume collecting payments. */
  resumes_at: number | null;
}

// ─────────────────────────────────────────────────────────────────────────────
// Payment settings
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Payment settings passed on to invoices created by the subscription.
 * @remarks Simplified from Stripe's payment_settings, which also exposes
 * payment_method_options for many fiat rails (ACH, cards, iDEAL, etc.) that Zoneless doesn't
 * support since all payments are settled in USDC. Matches the simplification on Invoice.
 */
export interface SubscriptionPaymentSettings {
  /**
   * Payment-method-specific configuration to provide to invoices created by the subscription.
   * Opaque stub for Stripe API parity; Zoneless settles invoices in USDC.
   */
  payment_method_options: object | null;
  /**
   * The list of payment method types to provide to every invoice created by the subscription. If
   * not set, Zoneless attempts to automatically determine the types to use.
   */
  payment_method_types: string[] | null;
  /**
   * Configure whether Zoneless updates subscription.default_payment_method when payment succeeds.
   * Defaults to off.
   */
  save_default_payment_method: 'off' | 'on_subscription' | null;
}

// ─────────────────────────────────────────────────────────────────────────────
// Pending invoice item interval
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Specifies an interval for how often to bill for any pending invoice items. It is analogous to
 * calling Create an invoice for the given subscription at the specified interval.
 */
export interface SubscriptionPendingInvoiceItemInterval {
  /** Specifies invoicing frequency. Either day, week, month or year. */
  interval: 'day' | 'week' | 'month' | 'year';
  /**
   * The number of intervals between invoices. For example, interval=month and interval_count=3
   * bills every 3 months. Maximum of one year interval allowed (1 year, 12 months, or 52 weeks).
   */
  interval_count: number;
}

// ─────────────────────────────────────────────────────────────────────────────
// Pending update
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Pending updates that will be applied to the subscription once the latest_invoice has been paid.
 */
export interface SubscriptionPendingUpdate {
  /**
   * If the update is applied, determines the date of the first full invoice, and, for plans with
   * month or year intervals, the day of the month for subsequent invoices. The timestamp is in
   * UTC format.
   */
  billing_cycle_anchor: number | null;
  /** The pending subscription-level discount that will be applied when the pending update is applied. */
  discount: CustomerDiscount | null;
  /**
   * The discounts that will be applied to the subscription when the pending update is applied.
   * Use `expand[]=discounts` to expand each discount.
   */
  discounts: (string | CustomerDiscount)[] | null;
  /** The point after which the changes reflected by this update will be discarded and no longer applied. */
  expires_at: number;
  /**
   * Set of key-value pairs that you can attach to an object. This can be useful for storing
   * additional information about the object in a structured format.
   */
  metadata: Record<string, string> | null;
  /** List of subscription items, each with an attached plan, that will be set if the update is applied. */
  subscription_items: SubscriptionItem[] | null;
  /**
   * Unix timestamp representing the end of the trial period the customer will get before being
   * charged for the first time, if the update is applied.
   */
  trial_end: number | null;
  /**
   * Indicates if a plan's trial_period_days should be applied to the subscription. Setting
   * trial_end per subscription is preferred, and this defaults to false. Setting this flag to
   * true together with trial_end is not allowed.
   */
  trial_from_plan: boolean | null;
}

