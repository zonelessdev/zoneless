// ─────────────────────────────────────────────────────────────────────────────

/** Settings related to subscription trials. */
export interface SubscriptionTrialSettings {
  /** Defines how the subscription should behave when the user's trial ends. */
  end_behavior: SubscriptionTrialEndBehavior;
}

/** Defines how the subscription should behave when the user's trial ends. */
export interface SubscriptionTrialEndBehavior {
  /**
   * Indicates how the subscription should change when the trial ends if the user did not provide
   * a payment method.
   */
  missing_payment_method: 'cancel' | 'create_invoice' | 'pause';
}
